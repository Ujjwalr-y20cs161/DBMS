
DECLARE
NUM number := &primenumcheck;
FLAG NUMBER := 1;
BEGIN
DBMS_OUTPUT.PUT_LINE('Prime number check');
IF (NUM = 1 OR  NUM = 0) THEN
 DBMS_OUTPUT.PUT_LINE(NUM||' IS NOT A PRIME');
ELSE
 FOR I IN 2..SQRT(NUM)
 LOOP
  IF NUM MOD I = 0 THEN
   FLAG := 0;
   EXIT;
  END IF;
 END LOOP;
 END IF;
 IF FLAG = 0 THEN
  DBMS_OUTPUT.PUT_LINE(NUM||' IS NOT A PRIME');
 ELSE
   DBMS_OUTPUT.PUT_LINE(NUM||' IS  A PRIME');
 END IF;
END;
/
