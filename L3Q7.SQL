DECLARE 
GROSS NUMBER;
NET NUMBER;
HRA NUMBER :=  0.15;
DA NUMBER :=  0.62;
PF NUMBER;
PTAX NUMBER := 0.02;
CURSOR EMPL IS SELECT * FROM EM;
NO EM.ENO%TYPE;
NAME EM.ENAME%TYPE;
PY EM.PAY%TYPE;
BEGIN 
OPEN EMPL;
LOOP
	FETCH EMPL INTO NO,NAME,PY;
	EXIT WHEN EMPL%NOTFOUND;
	HRA := HRA*PY;
	DA := DA*PY;
	PTAX := PTAX*PY;
	GROSS := HRA+DA+PY;
	IF (GROSS > 8000) THEN 
		PF := 750;
	ELSE PF:=600;
	END IF;
	NET := GROSS - PF-PTAX;
	DBMS_OUTPUT.PUT_LINE(PY||' '|| HRA ||' '|| DA||' '|| GROSS||' ' || PF||' '||PTAX||' '||NET);
END LOOP;
DBMS_OUTPUT.PUT_LINE('     ');
END;
/
