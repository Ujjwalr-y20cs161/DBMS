 DECLARE
NUM1 NUMBER := &NUM;
REV NUMBER:= 0;
LEN NUMBER := LENGTH(NUM1);
NUM2 NUMBER := NUM1;
BEGIN 
 FOR I IN 1..LEN
 LOOP
 REV := (REV*10)+MOD(NUM1,10);
 NUM1 := TRUNC(NUM1/10);
 END LOOP;
 IF NUM2 = REV THEN
 DBMS_OUTPUT.PUT_LINE(NUM2||' IS A PALINDROME');
 ELSE 
 DBMS_OUTPUT.PUT_LINE(NUM2||' IS NOT A PALINDROME');
 END IF;
END;
/
